entity.onTrade = function(player, npc, trade)
    local tradedCombo = 0

    -- check for af upgrade trades
    if player:getCharVar("[Monisette]item") == 0 then
        for k, v in pairs(tradeTable) do
            if npcUtil.tradeHasExactly(trade, v.trade) then
                tradedCombo = k
                break
			end
        end
    end

    -- found a match
    if tradedCombo > 0 then
	    local rewardItemId = tradeTable[tradedCombo].reward

        player:setCharVar("[Monisette]item", rewardItemId)
        player:startEvent(388)
    end
end

entity.onTrigger = function(player, npc, status)
    if player:getCharVar("[Monisette]item") ~= 0 then
        if os.time() > player:getCharVar("[Monisette]time") then
            player:startEvent(386)
        else
            player:startEvent(387)
        end
    else
        player:startEvent(385)
    end
end

