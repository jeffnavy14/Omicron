end

entity.onEventFinish = function(player, csid, option)
    if csid == 386 then
	    local reward = player:getCharVar("[Monisette]item")

	    if npcUtil.giveItem(player, reward) then
            player:setCharVar("[Monisette]item", 0)
            player:setCharVar("[Monisette]time", 0)
		end
    elseif csid == 388 then
        player:confirmTrade()
        player:setCharVar("[Monisette]time", getVanaMidnight())
    end
end

